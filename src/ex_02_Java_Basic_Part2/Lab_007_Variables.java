package ex_02_Java_Basic_Part2;

public class Lab_007_Variables {
    public static void main(String[] args) {
        //byte b = 128; //not allowed as max limit for byte is 127
        long phone_number = 9876543219L; //always put L or l in the end of long data type
        float f = 3.13F; //always put F or f in the end of float data type
        System.out.println(phone_number);
        System.out.println(f);
    }
}
