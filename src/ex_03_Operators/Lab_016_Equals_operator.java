public class Lab_016_Equals_operator {
    public static void main(String[] args) {
        // == compares the value and return boolean output
        System.out.println(10 == 10);
        System.out.println(10 > 10);
        System.out.println(10 >= 10);
        System.out.println(10 < 10);
    }
}
