package ex_03_Operators;

public class Lab_011_Unary_Operator {
    public static void main(String[] args) {
        // unary operators
        // unary + and -
        int a = + 10;
        int b = - 110;
        int result = a+b;
        System.out.println(result);

        // increment operator
        int d = 10;
        d++;
        System.out.println(d);
    }
}
